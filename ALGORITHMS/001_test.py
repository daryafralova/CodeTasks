#Натан любит кататься на велосипеде.
#Поскольку Натан знает, что важно поддерживать водный баланс, он выпивает 0,5 литра воды за час езды на велосипеде.
#Вам задано время в часах, и вам нужно вернуть количество литров, которое выпьет Натан, округлив до наименьшего значения.

#Например:
#time = 3 ----> litres = 1



def litres(time):
    return int(time * 0.5)