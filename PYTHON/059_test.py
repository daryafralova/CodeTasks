## 4.7 Дана структура. Найдите сумму элементов этой структуры

structura = [
	{
		2: (1, 2, 3),
		1: (1, 2, 3),
		3: (1, 2, 3),
	},
	{
		1: (1, 2, 3),
		2: (1, 2, 3),
		3: (1, 2, 3),
	},
	{
		1: (1, 2, 3),
		2: (1, 2, 3),
		3: (1, 2, 3),
	},
];


res = 0
for i in structura:
  res += sum(sum(i.values(), start=()))
print(res)